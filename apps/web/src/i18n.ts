function detectLocale(): AppLocale {
  if (typeof window !== "undefined") {
    const stored = window.localStorage.getItem(storageKey);
    if (stored === "en" || stored === "zh-CN") return stored;
    if (window.navigator.language.toLowerCase().startsWith("zh")) return "zh-CN";
  }
  return "en";
}

const localeState = ref<AppLocale>(detectLocale());

function interpolate(value: string, params: Record<string, string | number>): string {
  return value.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => String(params[key] ?? ""));
}

export function translate(key: string, params: Record<string, string | number> = {}, locale: AppLocale = localeState.value): string {
  const english = en[key] ?? key;
  return interpolate(locale === "zh-CN" ? zh[key] ?? zh[english] ?? english : english, params);
}

export function translateStatus(value: string, locale: AppLocale = localeState.value): string {
  return locale === "zh-CN" ? statusZh[value] ?? value.replaceAll("_", " ") : value.replaceAll("_", " ");
}

export function translateRole(value: string, locale: AppLocale = localeState.value): string {
  return locale === "zh-CN" ? roleZh[value] ?? value.replaceAll("_", " ") : value.replaceAll("_", " ");
}

export function setAppLocale(locale: AppLocale): void {
  localeState.value = locale;
  if (typeof window !== "undefined") window.localStorage.setItem(storageKey, locale);
  if (typeof document !== "undefined") {
    document.documentElement.lang = locale;
    document.title = locale === "zh-CN" ? "ScoutPi Earth 调查工作台" : "ScoutPi Earth Workbench";
  }
}

export function useI18n() {
  return {
    locale: readonly(localeState),
    isChinese: computed(() => localeState.value === "zh-CN"),
    t: translate,
    statusLabel: translateStatus,
    roleLabel: translateRole,
    setLocale: setAppLocale,
    toggleLocale: () => setAppLocale(localeState.value === "en" ? "zh-CN" : "en"),
  };
}

